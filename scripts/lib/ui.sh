#!/bin/bash

# =====================================================
# SAR - Servidor de Automação Residencial
# Biblioteca de Interface (UI)
# =====================================================

# -----------------------------------------------------
# Título principal
# -----------------------------------------------------
ui_titulo() {

    echo
    echo "=========================================================="
    printf "%s\n" "$1"
    echo "=========================================================="
    echo

}

# -----------------------------------------------------
# Subtítulo
# -----------------------------------------------------
ui_subtitulo() {

    echo
    echo "---------------------- $1 ------------------------------"
    echo

}

# -----------------------------------------------------
# Mensagem OK
# -----------------------------------------------------
ui_ok() {

    echo "✔ $1"

}

# -----------------------------------------------------
# Mensagem erro
# -----------------------------------------------------
ui_erro() {

    echo "✖ $1"

}

# -----------------------------------------------------
# Mensagem informativa
# -----------------------------------------------------
ui_info() {

    echo "ℹ $1"

}
